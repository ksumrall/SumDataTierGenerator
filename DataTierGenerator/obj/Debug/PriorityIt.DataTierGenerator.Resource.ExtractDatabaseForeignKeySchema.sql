SELECT TC.CONSTRAINT_SCHEMA SchemaName
    , TC.TABLE_NAME TableName
    , TC.CONSTRAINT_NAME IndexName
    , KCU.COLUMN_NAME ColumnName
    , KCU.ORDINAL_POSITION OrdinalPosition
    , TC2.CONSTRAINT_SCHEMA ReferencedSchemaName
    , TC2.TABLE_NAME ReferencedTableName
    , RC.UNIQUE_CONSTRAINT_NAME ReferencedIndexName
FROM INFORMATION_SCHEMA.TABLE_CONSTRAINTS TC
    INNER JOIN INFORMATION_SCHEMA.KEY_COLUMN_USAGE KCU
    ON TC.CONSTRAINT_SCHEMA = KCU.CONSTRAINT_SCHEMA
        AND TC.CONSTRAINT_NAME = KCU.CONSTRAINT_NAME
    INNER JOIN INFORMATION_SCHEMA.REFERENTIAL_CONSTRAINTS RC 
    INNER JOIN INFORMATION_SCHEMA.TABLE_CONSTRAINTS TC2
    ON RC.UNIQUE_CONSTRAINT_SCHEMA = TC2.CONSTRAINT_SCHEMA
        AND RC.UNIQUE_CONSTRAINT_NAME = TC2.CONSTRAINT_NAME
    ON KCU.CONSTRAINT_SCHEMA = RC.CONSTRAINT_SCHEMA
        AND KCU.CONSTRAINT_NAME = RC.CONSTRAINT_NAME
WHERE TC.CONSTRAINT_TYPE = 'FOREIGN KEY'
ORDER BY SchemaName, TableName, IndexName, OrdinalPosition
